    private void exteBBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exteBBActionPerformed
    if (NombreBEmpleado.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una extensión para buscar.");
        } else {

            try {
                float extBuscar = Float.parseFloat(NombreBEmpleado.getText());
                boolean encontrado = false;
                for (int i = 0; i <= n; i++) {
                    if (superficieDep[i] == extBuscar) {
                        
                        modeloTabla.setRowCount(0);
                        
                        Object[] fila = {direccion[i], superficieDep[i], precio[i]};
                        modeloTabla.addRow(fila);
                        encontrado = true;
                        break; // solo muestra el primero que coincide
                    }
                }
                if (!encontrado) {
                    JOptionPane.showMessageDialog(this, "No se encontró ningún departamento con esa extensión.");
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "La extensión debe ser un número válido.");
            }
        }
    }//GEN-LAST:event_exteBBActionPerformed

