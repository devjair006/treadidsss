    private void mostrarDep() {
    modeloTabla.setRowCount(0); // limpia la tabla

    // Creamos un arreglo de índices para ordenar
    Integer[] indices = new Integer[n + 1];
    for (int i = 0; i <= n; i++) {
        indices[i] = i;
    }

    // Ordenamos los índices en base a superficieDep
    Arrays.sort(indices, (a, b) -> Float.compare(superficieDep[a], superficieDep[b]));

    // Llenamos la tabla en el nuevo orden
    for (int idx : indices) {
        modeloTabla.addRow(new Object[]{
            direccion[idx],
            superficieDep[idx],
            precio[idx],
        });
    }
}
    
    
    
    
